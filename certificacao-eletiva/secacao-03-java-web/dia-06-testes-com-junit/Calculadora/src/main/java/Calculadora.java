/**
 * The type Calculadora.
 */
public class Calculadora {

  /**
   * Somar int.
   *
   * @param x the x
   * @param y the y
   * @return the int
   */
  public int somar(int x, int y) {
    return x + y;
  }

  /**
   * Subtrair int.
   *
   * @param x the x
   * @param y the y
   * @return the int
   */
  public int subtrair(int x, int y) {
    return x - y;
  }

  /**
   * Dividir int.
   *
   * @param x the x
   * @param y the y
   * @return the int
   */
  public int dividir(int x, int y) {
    return x / y;
  }
}